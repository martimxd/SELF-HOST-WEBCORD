const Context = createContext<{
  language: Language;
  setLanguage: (value: Language) => void;
  siteConfig: SiteConfig;
  refreshSiteConfig: () => Promise<void>;
  t: (key: string) => string;
} | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setState] = useState<Language>(() =>
    normalizeLanguage(localStorage.getItem('webcord-language')),
  );
  const [siteConfig, setSiteConfig] = useState<SiteConfig>(fallbackSiteConfig);

  const refreshSiteConfig = useCallback(async () => {
    try {
      const response = await fetch(`${API_URL}/site-config`, { credentials: 'include' });
      if (!response.ok) return;
      const result = await response.json() as Partial<SiteConfig>;
      const nextConfig: SiteConfig = {
        defaultLanguage: normalizeLanguage(result.defaultLanguage),
        siteIconUrl: apiAssetUrl(result.siteIconUrl),
        faviconUrl: apiAssetUrl(result.faviconUrl),
        loginBackgroundUrl: apiAssetUrl(result.loginBackgroundUrl),
      };
      setSiteConfig(nextConfig);
      if (!localStorage.getItem('webcord-language')) {
        setState(nextConfig.defaultLanguage);
      }
    } catch {
      setSiteConfig(fallbackSiteConfig);
    }
  }, []);

  useEffect(() => {
    void refreshSiteConfig();
  }, [refreshSiteConfig]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  useEffect(() => {
    const faviconUrl = siteConfig.faviconUrl || siteConfig.siteIconUrl;
    if (!faviconUrl) return;
    let link = document.querySelector<HTMLLinkElement>('link[rel="icon"]');
    if (!link) {
      link = document.createElement('link');
      link.rel = 'icon';
      document.head.appendChild(link);
    }
    link.href = faviconUrl;
  }, [siteConfig.faviconUrl, siteConfig.siteIconUrl]);

  const value = useMemo(() => ({
    language,
    setLanguage: (next: Language) => {
      localStorage.setItem('webcord-language', next);
      document.documentElement.lang = next;
      setState(next);
    },
    siteConfig,
    refreshSiteConfig,
    t: (key: string) => translations[language][key] ?? translations.en[key] ?? key,
  }), [language, refreshSiteConfig, siteConfig]);
  return <Context.Provider value={value}>{children}</Context.Provider>;
}

export function useI18n() {
  const value = useContext(Context);
  if (!value) throw new Error('Missing I18nProvider');
  return value;
}

export function LanguageSelect({ compact = false }: { compact?: boolean }) {
  const { language, setLanguage, t } = useI18n();
  return (
    <label className={`language-select ${compact ? 'compact' : ''}`}>
      <span>{t('language')}</span>
      <select value={language} onChange={(event) => setLanguage(event.target.value as Language)}>
        <option value="pt">Português</option>
        <option value="en">English</option>
        <option value="fr">Français</option>
      </select>
    </label>
  );
}
