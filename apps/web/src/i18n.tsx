const Context = createContext<{
  language: Language;
  setLanguage: (value: Language) => void;
  t: (key: string) => string;
} | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const detected: Language = navigator.language.startsWith('fr')
    ? 'fr'
    : navigator.language.startsWith('en')
      ? 'en'
      : 'pt';
  const [language, setState] = useState<Language>(() =>
    (localStorage.getItem('webcord-language') as Language) || detected,
  );
  const value = useMemo(() => ({
    language,
    setLanguage: (next: Language) => {
      localStorage.setItem('webcord-language', next);
      document.documentElement.lang = next;
      setState(next);
    },
    t: (key: string) => translations[language][key] ?? translations.en[key] ?? key,
  }), [language]);
  return <Context.Provider value={value}>{children}</Context.Provider>;
}

export function useI18n() {
  const value = useContext(Context);
  if (!value) throw new Error('Missing I18nProvider');
  return value;
}

export function LanguageSelect({ compact = false }: { compact?: boolean }) {
  const { language, setLanguage, t } = useI18n();
  return (
    <label className={`language-select ${compact ? 'compact' : ''}`}>
      <span>{t('language')}</span>
      <select value={language} onChange={(event) => setLanguage(event.target.value as Language)}>
        <option value="pt">Português</option>
        <option value="en">English</option>
        <option value="fr">Français</option>
      </select>
    </label>
  );
}
